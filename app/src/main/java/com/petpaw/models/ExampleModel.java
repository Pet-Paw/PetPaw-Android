package com.petpaw.models;

public class ExampleModel {
    private String name;
    private final int id;

    public ExampleModel(String name, int id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }
}
